let word = "Testando";

// Acesse o primeiro caractere usando o índice 0
let firstCharacter = word.charAt(0);

// Acesse o último caractere usando o índice do comprimento da string menos 1
let lastCharacter = word.charAt(word.length - 1);

// Mostra os caracteres no console
console.log("Primeiro caractere:", firstCharacter);
console.log("Último caractere:", lastCharacter);